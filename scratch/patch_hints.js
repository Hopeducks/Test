let content = fs.readFileSync('data/questions.ts', 'utf-8');
let count = 0;

for (const [id, hint] of Object.entries(hints)) {
  const idPattern = `id: '${id}'`;
  const idIdx = content.indexOf(idPattern);
  if (idIdx === -1) { console.warn('Not found:', id); continue; }

  const blockEnd = content.indexOf('  },', idIdx);
  const block = content.slice(idIdx, blockEnd);
  if (block.includes('hint:')) continue;

  // Find explanation line end: match explanation: '...',  (single-line)
  const expRegex = /explanation: '([^']|\\')*',/;
  const expMatch = expRegex.exec(block);
  if (!expMatch) { console.warn('No explanation found for:', id); continue; }

  const expStr = expMatch[0];
  const expPosInContent = content.indexOf(expStr, idIdx);
  if (expPosInContent === -1) { console.warn('Exp not found in content for:', id); continue; }

  const insertAt = expPosInContent + expStr.length;
  const hintLine = `\n    hint: '${hint}',`;
  content = content.slice(0, insertAt) + hintLine + content.slice(insertAt);
  count++;
}

fs.writeFileSync('data/questions.ts', content);
